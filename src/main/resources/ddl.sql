CREATE TABLE matcha.USER(ID MEDIUMINT AUTO_INCREMENT, CONFIRM VARCHAR(55), USERNAME VARCHAR(30), NAME VARCHAR(30), LASTNAME VARCHAR(30), MIDDLENAME VARCHAR(30), BIRTHDAY DATE, YEARS_OLD MEDIUMINT,
                          GENDER VARCHAR(40), SEXUAL_PREFERENSE VARCHAR(40), EMAIL VARCHAR(30), PASSWORD VARCHAR(255), LOCATION VARCHAR(180), USER_CARD MEDIUMINT, FILTER_PARAMS MEDIUMINT, PRIMARY KEY (ID));

CREATE TABLE matcha.USER_CARD(ID MEDIUMINT AUTO_INCREMENT, BIOGRAPHY varchar(300), WORKPLACE varchar(70), POSITION varchar(70), EDUCATION VARCHAR(120), TAGS VARCHAR(400), RATING DOUBLE, USER_ID , PRIMARY KEY (ID));

CREATE TABLE matcha.LINK(ID INTEGER AUTO_INCREMENT, URL VARCHAR(200), OPEN MEDIUMINT, PRIMARY KEY (ID));

CREATE TABLE matcha.JWT(ID INTEGER AUTO_INCREMENT, USER_ID MEDIUMINT, TOKEN VARCHAR(255), PRIMARY KEY (ID));

CREATE TABLE matcha.LIKES_ACTION(FROM_USR MEDIUMINT, TO_USR MEDIUMINT, ACTION VARCHAR(30), CREATION_TIME CURRENT_TIMESTAMP);

CREATE TABLE matcha.LIKES(FROM_USR MEDIUMINT, TO_USR MEDIUMINT);

CREATE TABLE matcha.FILTER_PARAMS(ID MEDIUMINT AUTO_INCREMENT, AGE_BY MEDIUMINT, AGE_TO MEDIUMINT, RATING DOUBLE, COMMON_TAGS_COUNT MEDIUMINT, PRIMARY KEY(ID));

CREATE TABLE matcha.WEB_SOCKET_MESSAGE(CHAT_ID MEDIUMINT, FROM_ID MEDIUMINT, TYPE VARCHAR(30), CONTENT BLOB);

CREATE TABLE matcha.CHAT_AFFILIATION(USER_ID MEDIUMINT, CHAT_ID MEDIUMINT);
