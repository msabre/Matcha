CREATE TABLE matcha.USER(ID MEDIUMINT AUTO_INCREMENT, CONFIRM VARCHAR(55), USERNAME VARCHAR(30), NAME VARCHAR(30), LASTNAME VARCHAR(30), MIDDLENAME VARCHAR(30), EMAIL VARCHAR(30), PASSWORD VARCHAR(255), COUNTRY VARCHAR(30), USER_CARD MEDIUMINT, PRIMARY KEY (ID), FOREIGN KEY(USER_CARD) REFERENCES USER_CARD (ID));

CREATE TABLE matcha.USER_CARD(ID MEDIUMINT AUTO_INCREMENT, GENDER VARCHAR(30), SEXUAL_PREFERENCE varchar(30), BIOGRAPHY varchar(300), WORKPLACE varchar(70), POSITION varchar(70), EDUCATION VARCHAR(120), LOCATION VARCHAR(180), PRIMARY KEY (ID));

CREATE TABLE matcha.USER_TAGS(ID MEDIUMINT AUTO_INCREMENT, TAG VARCHAR(30), PRIMARY KEY (ID));

CREATE TABLE matcha.PHOTO(ID MEDIUMINT AUTO_INCREMENT, FILE_PATH VARCHAR(255), FILE_FORMAT VARCHAR(30), PRIMARY KEY (ID));

CREATE TABLE matcha.LINK(ID INTEGER AUTO_INCREMENT, URL VARCHAR(200), OPEN MEDIUMINT, PRIMARY KEY (ID));

CREATE TABLE matcha.JWT(ID INTEGER AUTO_INCREMENT, USER_ID MEDIUMINT, TOKEN VARCHAR(255), PRIMARY KEY (ID));

